# How Computers Store Data (Title)

# Checkpoint

# 1.12 What amount of memory is enough to store a letter of the alphabet 
# or a small number?
# A: Byte

# 1.13 What do you call a tiny "switch" that can be set to either on or 
# off? 
# A: Bit

# 1.14 In what numbering system are all numeric values written as 
# sequences of 0s and 1s?
# A: Binary Numbering System

# 1.15 What is the purpose of ASCII?
# A: An encoding scheme that uses a set of 128 numeric codes 
# to represent the English letters, various punctuation marks, 
# and other characters. These numeric codes are used to store 
# characters in a computer's memory.

# 1.16 What encoding scheme is extensive enough to represent the 
# characters of many of the languages in the world?
# A: Unicode

# 1.17 What do the terms "digital data" and "digital device" mean?
# A: Digital data is any data that is stored in binary format and 
# digital device is any device that works with binary data.

